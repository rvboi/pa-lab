# Using a for loop to print numbers from 1 to 10
print("Numbers from 1 to 10 using a for loop:")
for i in range(1, 11):
    print(i, end=" ")
print()  # For a newline

# Using a while loop to print numbers from 1 to 10
print("Numbers from 1 to 10 using a while loop:")
counter = 1
while counter <= 10:
    print(counter, end=" ")
    counter += 1
print()  # For a newline
